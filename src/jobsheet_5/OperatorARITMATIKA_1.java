package jobsheet_5;
public class OperatorARITMATIKA_1 {
    public static void main(String [] args){
     int a = 10;
     int b = 2;
     int c;
     
     //penjumlahan
     c = a + b;
     System.out.println(a + " + " + b + " = " + c);
     
     //pengurangan
     c = a - b;
     System.out.println(a + " - " + b + " = " + c);
     
     //perkalian
     c = a * b;
     System.out.println(a + " * " + b + " = " + c);
     
     //pembagian
     c = a / b;
     System.out.println(a + " / " + b + " = " + c);
     
     //sisa pembagian
     c = a % b;
     System.out.println(a + " % " + b + " = " + c);          
    }
}

